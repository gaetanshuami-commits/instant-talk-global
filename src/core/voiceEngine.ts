async function startVapiBridge(
  sourceLang: string,
  targets: string[],
  callbacks: SubtitleCallbacks,
  ttsLang?: string,
  voiceGender: VoiceGender = "female",
  getRemoteCount?: () => number
): Promise<boolean> {
  if (typeof window === "undefined") return false

  const key = process.env.NEXT_PUBLIC_VAPI_KEY
  if (!key) return false

  try {
    const mod = await import("@vapi-ai/web")
    const Vapi = mod.default
    const vapi = new Vapi(key)

    const voiceMap = voiceGender === "male" ? TTS_VOICE_MALE : TTS_VOICE_FEMALE

    vapi.on("message", async (msg: any) => {
      if (!msg || msg.type !== "transcript") return
      if (msg.role && msg.role !== "user") return

      const text = String(msg.transcript || "").trim()
      if (!text) return

      callbacks.onPartial(sourceLang, text)

      if (msg.transcriptType !== "final") return

      const res = await fetch("/api/translate", {
        method: "POST",
        headers: { "Content-Type": "application/json" },
        body: JSON.stringify({ text, targetLangs: targets }),
      })

      const data = await res.json()
      const batch: Record<string, string> = data.translations ?? {}

      for (const lang of targets) {
        const translated = batch[lang] ?? text
        callbacks.onFinal(lang, translated)

        if (ttsLang && lang === ttsLang) {
          const rc = getRemoteCount ? getRemoteCount() : 1
          if (rc > 0) enqueueTTS(translated, lang, voiceMap)
        }
      }
    })

    vapi.on("error", (err: any) => console.warn("[VAPI] error", err))

    await vapi.start({
      transcriber: {
        provider: "deepgram",
        model: "nova-2",
        language: sourceLang === "fr" ? "fr" : sourceLang === "en" ? "en" : "multi",
      },
    })

    console.log("[VAPI] STT active")
    return true
  } catch (err) {
    console.warn("[VAPI] start failed", err)
    return false
  }
}

